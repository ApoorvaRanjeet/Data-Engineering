# int 
b=1
print(type(b))
#float
c=10.8
print(type(c))
#complex
d=1j
print(type(d))
#list
e=[1,2,3,4]
print(type(e))
#tuple
f=(1,2,3,4)
print(type(f))
#dict
g={1:'a',2:'b',3:'c'}
print(type(g))
#set
h={12,3,4}
print(type(h))
#frosenset
i=frozenset({1,2,3,4})
print(type(i))
#range
j=range(6)
print(type(j))
#bool
k=True
print(type(k))
#bytes
l=b"hello"
print(l)
print(type(l))
#bytearray
m=bytearray(5)
print(m)
print(type(m))
#memoryview
n=memoryview(bytes(5))
print(n)
print(type(n))
#none
o=None
print(type(o))



